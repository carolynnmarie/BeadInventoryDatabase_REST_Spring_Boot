package com.beadinventory.beadinventory.REST.Domain.Supplies.SupplyEnums;


import java.io.Serializable;

public enum FindingCategory implements Serializable {
    LOBSTER_CLASP("closure"),
    LOOP_AND_TOGGLE_CLASP("closure"),
    MAGNETIC_CLASP("closure"),
    THREADED_BARREL_CLASP("closure"),
    RING_CLASP("closure"),
    CARABINER("closure"),
    EAR_WIRE("earring finding"),
    EARRING_POST("earring finding"),
    EARRING_BACK("earring finding"),
    CRIMP_BEAD("crimp"),
    SPRING_COIL("crimp"),
    GROOVED_CRIMP_WITH_LOOP("crimp"),
    CRIMP_COVER("crimp"),
    HEAD_PIN("pin"),
    EYE_PIN("pin"),
    SAFETY_PIN("pin"),
    BAR_PIN("pin"),
    JUMP_RING("ring"),
    CLOSED_JUMP_RING("ring"),
    SPLIT_RING("ring"),
    KEY_RING("ring"),
    BAIL("bail"),
    ALLIGATOR_CLIP("clip"),
    LANYARD_SNAP_CLIP("clip"),
    LANYARD_LOBSTER_CLASP("clip"),
    CONNECTOR("other"),
    SPACER("other");


    String type;

    FindingCategory(String type){
        this.type = type;
    }

    public String getType() {
        return type;
    }

    @Override
    public String toString(){
        return name().toLowerCase().replace('_',' ');
    }
}
